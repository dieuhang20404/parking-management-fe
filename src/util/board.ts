export type SlotIndex = {
    id: number;
    r: number;
    c: number;
};

export const SLOT_INDEX: SlotIndex[] = (() => {
    let id = 1;
    const slots: SlotIndex[] = [];

    PARKING_LAYOUT.forEach((row, r) => {
        row.forEach((cell, c) => {
            if (cell === 1) {
                slots.push({
                    id: id++,
                    r,
                    c,
                });
            }
        });
    });

    return slots;
})();
